	<h2>Introducción</h2>
	<p class="justificar samesubtopic">
		Hola ¿qué tal?. Hace un tiempo, junto a un amigo, decidímos crear este sitio, para compartir
		algunos recursos y materiales para <em> llevarsela </em> más fácil en su estancia en Bátiz, puesto
		que las cosas se puedenen complicar seguido y bastante.
	</p>

	<p class="justificar samesubtopic">
		En estos espacios podrás encontrar apuntes, resúmenes, exámenes y otros materiales que hemos
		recopilado durante nuestra estancia en la escuela, además de materiales donados por compañeros y
		excompañeros. Estos recursos nos han ayudado a comprender mejor algunos temas, y esperamos que
		también puedan ser útiles para ustedes.
	</p>

	<p class="justificar">Apreciaríamos mucho cualquier aporte de material o sugerencia.</p>

	<h2>¿Cómo navegar dentro de la página?</h2>
	<p class="justificar">
		El compendio esta hecho para que se pueda navegar en cualquier dispositivo, ya sea móvil,
		computadora o laptob. Ésta navegación en ambos tipos de dispositivos es bastante sencilla de
		reconocer y utilizar. A continuación se mostrará el manejo de la página para ambos dispositivos.
	</p>
	<p class="justificar">
		En teléfonos celulares se encuentra un menú desplegable en la parte superior derecha que, al
		hacer clic se mostrarán los seis semestres y al hacer clic en cada semestre saldrán sus
		respectivas unidades de aprendizaje. Por otro lado, en las computadoras y laptobs la forma de
		navegación es más sencilla de ver, ya que los semestres se encuentran en la parte superior de la
		página y al pasar el cursor sobre ellos, se mostrarán cada una de las unidades con su respectivo
		semestre.
	</p>
	<p class="justificar">
		No olvides contactar a alguno de los creadores del sitio si encuentras algún tipo de error al
		navegar dentro del compendio.
	</p>
